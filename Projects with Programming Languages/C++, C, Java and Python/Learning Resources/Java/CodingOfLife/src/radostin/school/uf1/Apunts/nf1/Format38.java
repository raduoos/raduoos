package radostin.school.uf1.Apunts.nf1;

public class Format38 {
    public static void main(String[] args) {
        System.out.printf("%e %E", 10.0, 10.0);
        System.out.println();
        System.out.printf("%g %G", 10.0, 10.0);
        System.out.println();
        System.out.printf("%g %G", 10000000000000.0, 100000000.0);
        System.out.println();
        System.out.printf("%f", 100000000000.0);
    }
}
