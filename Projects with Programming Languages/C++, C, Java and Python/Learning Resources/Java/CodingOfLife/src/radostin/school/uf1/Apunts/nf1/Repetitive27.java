package radostin.school.uf1.Apunts.nf1;

public class Repetitive27 {
    public static void main(String[] args) {
        //Countdown from 5 to 1:

        //With fori:
        for (int a=5; a>0; a=a-1) {
            System.out.println(a);
        }

        //With while:
        int a = 5;
        while (a>0){
            System.out.println(a);
            a=a-1;
        }
    }
}
