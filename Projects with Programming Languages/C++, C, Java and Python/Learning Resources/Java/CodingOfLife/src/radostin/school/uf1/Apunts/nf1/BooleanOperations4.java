package radostin.school.uf1.Apunts.nf1;

public class BooleanOperations4 {
    public static void main(String[] args) {

        boolean variable = true; //false
        boolean segona = false;

        //Muestra si la variable es true.
        System.out.println(variable);
        //Muestra si variable no es true.
        System.out.println(!variable);

        //I lògica
        System.out.println( variable && segona ); //and

        //O lògica
        System.out.println( variable || segona ); //or

    }
}
