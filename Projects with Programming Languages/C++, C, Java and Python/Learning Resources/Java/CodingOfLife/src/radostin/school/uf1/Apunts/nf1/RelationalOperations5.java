package radostin.school.uf1.Apunts.nf1;

public class RelationalOperations5 {
    public static void main(String[] args) {
        // Equal ==
        // Difference !=
        // Smaller than <
        // Smaller or equal <=
        // Higher >
        // Higher or equal >=

        int i = 23;
        boolean op;

        op = (i == 23); //returns true
        op = (i != 23); //returns false
        op = (i < 23); //returns false
        op = (i <= 23); //returns true
        op = (i > 23); //returns false
        op = (i >= 23); //returns true

    }
}
